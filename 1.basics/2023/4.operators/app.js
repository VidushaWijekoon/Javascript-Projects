// Arithmetic Operators

let x = 11;
let y = 3;

console.log("Addition = ", x + y);
console.log("Subtraction = ", x - y);
console.log("Multiplication = ", x * y);
console.log("Exponentiation = ", x ** y);
console.log("Division = ", x / y);
console.log("Modulus = ", x % y);
console.log("Increment = ", x++);
console.log("Decrement = ", x--);

// Assignment Operators
console.log("Assignment Addition = ", (x += 5));
console.log("Assignment Subtraction = ", (x -= 5));
console.log("Assignment Multiplication = ", (x *= 5));
console.log("Assignment Exponentiation = ", (x **= 5));
console.log("Assignment Division = ", (x /= 5));
console.log("Assignment Modulus = ", (x %= 4));

// Comparison Operators
console.log("Equal to = ", x == 10);
console.log("Equal to = ", y == 3);
console.log("Equal value and equal type = ", y === 3);
console.log("Not Equal = ", y != 3);
console.log("Not equal value and equal type = ", y !== "3");
console.log("Greater than = ", 10 > 3);
console.log("Greater than equal to = ", 10 >= 3);
console.log("Less than = ", 10 < 3);
console.log("Less than equal to = ", 10 <= 3);
